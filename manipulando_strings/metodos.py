nome = 'joÃo'
print(nome)
#método  upper() todas as letras ficam  em maiusculo
print(nome.upper())
#Método lower()  todas as letras ficam em minusculo
print(nome.lower())
#métedo title()  Deixa a primeira letra maiscula 
print(nome.title())


nome = '  joÃo   p'
#metodo strip() usado para remover espaços em brancos dentro de uma string 
print(nome.strip())
#metodo lstrip() remove só o spaço da esquerda
print(nome.lstrip())
#metodo rstrip() remove o espaço da direita 
print(nome.rstrip())