#Nessa aula vamos aprender sobre os operadores aritméticos no python 

#Adição -- usamos o sinal tradicional  +
#exemplo:
n1 = 2 
n3 = 4
print(n1 + n3)

#Subtração -- Usamos o sinal tradicional  -
#Exemplo: 
print(n3 - n1)


#Mutiplicação -- Usamos o sinal *
#Exemplo:
print(n1 * n3)

#Divisão  -- Usamos a /                Quando usamos 2 barras, entrega uma resposta de valor inteiro, ou seja, sem os números após a virgula 
#exemplo:
print(n3 / n1 )
print(n3 // n1 )


#Módulo ou o resto de uma divisão --- Usamos o %
#Exemplo 
print(n3 % n1)


#Potenciação -- Usamos **
#Exemplo:
print(n1 ** n3)